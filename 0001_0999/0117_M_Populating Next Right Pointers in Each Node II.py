"""
# Definition for a Node.
class Node:
    def __init__(self, val: int = 0, left: 'Node' = None, right: 'Node' = None, next: 'Node' = None):
        self.val = val
        self.left = left
        self.right = right
        self.next = next
"""

class Solution:
    def connect(self, root: 'Node') -> 'Node':
        if root is None:
            return None
        
        queue = [root]
        while queue:
            for i, node in enumerate(queue[1:]):
                queue[i].next = node
            queue[-1].next = None

            next_queue = []
            for node in queue:
                if node.left:
                    next_queue.append(node.left)
                if node.right:
                    next_queue.append(node.right)
            
            queue = None
            if len(next_queue) > 0:
                queue = next_queue
        
        return root