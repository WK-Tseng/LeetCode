class Solution:
    def arrangeCoins(self, n: int) -> int:
        # 0: 0
        # 1: 0 + 1 = 1
        # 2: 1 + 2 = 3
        # 3: 3 + 3 = 6
        # 4: 6 + 4 = 10
        # (1+h)*h/2
        # (1+h)*h/2 >= n
        # (h+h**2) >= n * 2
        # (h**2+h+(1/2)**2) >= n * 2 + (1/2)**2 : a**2 + 2ab + b**2 = (a+b)**2
        # (h+1/2)**2 >= n*2 + (1/2)**2 
        # h+0.5 >= (2n+0.25)**0.5
        # h >= (2n+0.25)**0.5 - 0.5

        return math.floor((2*n+0.5)**0.5 - 0.5)
